package com.lymin.condition;

public class ForTest {
	public static void main(String[] args) {

		System.out.println("반목 출력 합니다." + 1);
		System.out.println("반목 출력 합니다." + 2);
		System.out.println("반목 출력 합니다." + 3);
		System.out.println("반목 출력 합니다." + 4);
		System.out.println("반목 출력 합니다." + 5);

		for (int i = 1; i < 6; i++) {
			System.out.println("For 반목 출력 합니다." + i);
		}
	}

}
