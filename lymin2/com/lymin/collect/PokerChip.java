package com.lymin.collect;

public class PokerChip {
	private int value;

	public PokerChip(int value) {
		super();
		this.value = value;
	}

	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}

	/**
	 * @param value the value to set
	 */
	public void setValue(int value) {
		this.value = value;
	}

}
